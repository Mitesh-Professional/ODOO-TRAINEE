#Escape Sequences:-

#Examples:
#\n: Newline
#\t: Tab
#\\: Literal backslash
#\": Literal double quote
#\': Literal single quote

print("Hello, \nMy Name Is Mitesh Amin.")
print("Hello,\tMy Name Is Mitesh Amin.")
print("Hello, \\My Name Is Mitesh Amin.")
print("Hello, \"My Name Is Mitesh Amin.")
print("Hello, \'My Name Is Mitesh Amin.")

